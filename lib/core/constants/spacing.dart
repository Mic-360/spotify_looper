/// Material 3 Expressive spacing constants following 8dp grid system.
///
/// Reference: M3E Guidelines - Spacing & Layout section
class Spacing {
  Spacing._();

  /// Extra small - 4dp (internal padding in compact components)
  static const double xs = 4;

  /// Small - 8dp (small gaps, compact layouts)
  static const double s = 8;

  /// Medium - 12dp (default spacing between elements)
  static const double m = 12;

  /// Large - 16dp (card padding, list item spacing)
  static const double l = 16;

  /// Extra large - 24dp (section spacing, generous padding)
  static const double xl = 24;

  /// Double extra large - 32dp (major section breaks)
  static const double xxl = 32;

  /// Triple extra large - 48dp (large dividers, hero spacing)
  static const double xxxl = 48;

  /// Architectural spacing - 64dp+
  static const double architectural = 64;
}
